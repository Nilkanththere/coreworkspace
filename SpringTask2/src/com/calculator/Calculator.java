package com.calculator;

public class Calculator {

	int x=50;
	int y=10;
	
	
	public void add()
	{
	   int z=x+y;
	   System.out.println("Addition="+z);
	}
	
	public void sub()
	{
	   int z=x-y;
	   System.out.println("Substraction="+z);
	}
	
	public void mul()
	{
	   int z=x*y;
	   System.out.println("Multiplication="+z);
	}
	
	public void div()
	{
	   int z=x/y;
	   System.out.println("Division="+z);
	}
	
	
}
