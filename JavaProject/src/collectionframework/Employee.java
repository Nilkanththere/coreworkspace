package collectionframework;

public class Employee {

	private  int emid;
	private  String emname;
	private  String emdesig;
	private  int emsalary;
	public int getEmid() {
		return emid;
	}
	public void setEmid(int emid) {
		this.emid = emid;
	}
	public String getEmname() {
		return emname;
	}
	public void setEmname(String emname) {
		this.emname = emname;
	}
	public String getEmdesig() {
		return emdesig;
	}
	public void setEmdesig(String emdesig) {
		this.emdesig = emdesig;
	}
	public int getEmsalary() {
		return emsalary;
	}
	public void setEmsalary(int emsalary) {
		this.emsalary = emsalary;
	}
	
}
